public class data_types{
    public static void main(String args[]){
        // Byte Data type
        byte b = 8;
        System.out.println(b);
        // Char Data type - stores character type in here
        char ch = 'a';
        System.out.println(ch);
        // Boolean Data type - stores true/false values 
        boolean var = true;
        System.out.println(var);
        // Float Data type - stores decimal type values
        float price = 10.5f;
        System.out.println(price);
        // Integer Data type - stores integer type values (no decimals are included)
        int number = 10;
        System.out.println(number);
        // long
        long long_int = 12345678910L;
        System.out.println(long_int);
        // double
        // short
    }
}