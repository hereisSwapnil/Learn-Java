public class output{
    public static void main(String args[]){
        // Prints the string directly on the console
        System.out.print("Hello World");
        
        // Prints the text inside of the brackets and then moves to a new line 
        System.out.println("Hello World");
        
        // The "\n" Character will add a new line after it prints the text inside the brackets
        System.out.print("Hello World\n");
    }
}