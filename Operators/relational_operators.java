public class relational_operators{
    public static void main(String args[]){
        int A = 10;
        int B = 20;
        System.out.println(A>=B);
        // Types of relational operators:
        /*
         *   ==   equal to
         *   !=   not equal to
         *   >   greater than
         *   <   less than
         *   >=   greater than equal to
         *   <=   less than equal to
         */
    }
}