public class assignment_operators{
    public static void main(String args[]){
        int A = 10;
        int B = 10;
        
        // Both te statements are same and A and B will become 12
        A = A + 2;
        B+=2;
        System.out.println(A + " " + B);
        // Types of Assignment Operator
        /* 
           =    -   this will assign the value of right variable into left variable
           +=   -   same as A = A + 2
           -=   -   same as A = A - 2
           *=   -   same as A = A * 2
           /=   -   same as A = A / 2
        */
    }
}