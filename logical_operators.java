public class logical_operators{
    public static void main(String args[]) {
        System.out.println((3>2) && (3<1));
        // Types of logical operators:
        /*
         *   &&  -   Logical AND   -  if both are true then output is true
         *   ||  -   Logical OR    -  if any one of the statements become true then the output becomes true
         *   !   -   Logical NOT   -  reverses true to false and flase to true
         */
    }
}
